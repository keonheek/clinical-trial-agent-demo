#!/usr/bin/env python3
"""
eval.py — the 30%-weighted evaluation axis.

Compares the pipeline's PRE-REEVAL matcher verdicts (traces.json trials[].criteria[]) against
eval_labels.json, a small hand-labeled set of ~40 (patient, trial, criterion) rows across >=6
patients. Labels were written by a human reading the criterion text + original vignette alone
(see make_eval_worksheet.py) -- BLIND to what the matcher said -- so this comparison is not
circular. Where the vignette genuinely does not say, the label is UNKNOWN (strict).

Computes: overall accuracy, a confusion matrix (per verdict-class), and a short error analysis
written directly from the confusion numbers (not generated by an LLM).

Run:
    python3 eval.py
Writes:
    eval_results.json  {accuracy, n, confusion, errors: [...], analysis}
"""
import json
import os
from collections import defaultdict

from pipeline import effect_of

HERE = os.path.dirname(os.path.abspath(__file__))
VERDICTS = ["MET", "NOT_MET", "UNCERTAIN", "UNKNOWN"]


def load_traces():
    with open(os.path.join(HERE, "traces.json")) as f:
        return json.load(f)


def build_pipeline_lookup(traces):
    """(patient_id, nct_id, criterion_text) -> pipeline verdict (pre-reeval)."""
    lookup = {}
    for t in traces:
        pid = t["patient_id"]
        for trial in t["trials"]:
            for c in trial["criteria"]:
                lookup[(pid, trial["nct_id"], c["text"])] = c["verdict"]
    return lookup


def compute_safety_metrics(labels, traces):
    """Metrics that overall accuracy hides.

    Accuracy weights every criterion equally, so a model can score well while systematically
    missing the criteria that actually make a patient ineligible. In trial matching the failure
    modes are not symmetric: telling an ineligible patient they qualify is far worse than being
    unsure about an eligible one. These three separate the dangerous errors from the harmless.

    All three are derived from the existing labels plus the effect table -- no relabelling
    needed, because `effect` is a pure function of (criterion type, criterion-truth verdict).
    """
    # (patient, nct, criterion) -> (type, predicted verdict)
    info = {}
    for t in traces:
        for trial in t["trials"]:
            for c in trial["criteria"]:
                info[(t["patient_id"], trial["nct_id"], c["text"])] = (c["type"], c["verdict"])

    missed_fails = []          # truth says this criterion excludes the patient; we did not
    false_certainty = []       # record is silent; we asserted a definite verdict anyway
    n_true_fail = 0
    n_true_unknown = 0

    for row in labels:
        key = (row["patient_id"], row["nct_id"], row["criterion_text"])
        if key not in info:
            continue
        ctype, predicted = info[key]
        expected = row["expected_verdict"]

        expected_effect = effect_of(ctype, expected)
        predicted_effect = effect_of(ctype, predicted)

        if expected_effect == "FAIL":
            n_true_fail += 1
            if predicted_effect != "FAIL":
                missed_fails.append({
                    "patient_id": row["patient_id"], "nct_id": row["nct_id"],
                    "criterion_text": row["criterion_text"], "type": ctype,
                    "expected": expected, "predicted": predicted,
                    "predicted_effect": predicted_effect,
                })

        if expected == "UNKNOWN":
            n_true_unknown += 1
            if predicted in ("MET", "NOT_MET"):
                false_certainty.append({
                    "patient_id": row["patient_id"], "nct_id": row["nct_id"],
                    "criterion_text": row["criterion_text"], "predicted": predicted,
                })

    def rate(hit, total):
        return round(hit / total, 4) if total else None

    # Not all misses are equal, and collapsing them into one number hides the only distinction
    # that matters clinically. If a criterion should disqualify the patient and we instead said
    # PASS, we have waved an ineligible patient through: that is the failure that could actually
    # hurt someone. If we said REVIEW, we did not decide -- the criterion is flagged and the
    # system asks about it. That is the system working as designed, not a safety failure.
    wrongly_passed = [m for m in missed_fails if m["predicted_effect"] == "PASS"]
    deferred = [m for m in missed_fails if m["predicted_effect"] == "REVIEW"]

    return {
        # Of the criteria that should disqualify the patient, how many did we catch outright?
        "disqualifying_criterion_recall": rate(n_true_fail - len(missed_fails), n_true_fail),
        "n_disqualifying_criteria": n_true_fail,
        # THE number that decides whether this is safe to put in front of anyone.
        # A disqualifying criterion we confidently marked as passing.
        "wrongly_passed_rate": rate(len(wrongly_passed), n_true_fail),
        "n_wrongly_passed": len(wrongly_passed),
        "wrongly_passed": wrongly_passed,
        # Disqualifying criteria we declined to decide. These become clarifying questions.
        "n_deferred_to_review": len(deferred),
        "missed_disqualifying_criteria": missed_fails,
        # How often did we invent certainty the record does not support?
        "false_certainty_rate": rate(len(false_certainty), n_true_unknown),
        "n_record_silent_criteria": n_true_unknown,
        "false_certainty_cases": false_certainty,
    }


def main():
    with open(os.path.join(HERE, "eval_labels.json")) as f:
        labels = json.load(f)

    traces = load_traces()
    pipeline_lookup = build_pipeline_lookup(traces)

    confusion = defaultdict(lambda: defaultdict(int))  # confusion[expected][predicted] = count
    errors = []
    n_matched = 0
    n_correct = 0
    n_missing = 0

    for row in labels:
        key = (row["patient_id"], row["nct_id"], row["criterion_text"])
        predicted = pipeline_lookup.get(key)
        if predicted is None:
            n_missing += 1
            errors.append({
                "patient_id": row["patient_id"], "nct_id": row["nct_id"],
                "criterion_text": row["criterion_text"], "expected": row["expected_verdict"],
                "predicted": None, "note": "no matching row found in traces.json (criterion text drift?)",
            })
            continue
        expected = row["expected_verdict"]
        n_matched += 1
        confusion[expected][predicted] += 1
        if predicted == expected:
            n_correct += 1
        else:
            errors.append({
                "patient_id": row["patient_id"], "nct_id": row["nct_id"],
                "criterion_text": row["criterion_text"], "expected": expected, "predicted": predicted,
            })

    accuracy = (n_correct / n_matched) if n_matched else 0.0

    confusion_out = {v: {v2: confusion[v][v2] for v2 in VERDICTS if confusion[v][v2]} for v in VERDICTS}

    safety = compute_safety_metrics(labels, traces)

    # ---- error analysis, computed directly from the confusion numbers, not LLM-generated ----
    total_labeled = len(labels)
    per_class_recall = {}
    for v in VERDICTS:
        support = sum(confusion[v].values())
        if support:
            per_class_recall[v] = confusion[v].get(v, 0) / support
    worst_class = min(per_class_recall, key=per_class_recall.get) if per_class_recall else None
    unknown_confused_as_definite = sum(
        confusion["UNKNOWN"].get(v, 0) for v in ("MET", "NOT_MET")
    )
    n_wrong = n_matched - n_correct

    analysis_sentences = []
    analysis_sentences.append(
        f"Overall accuracy is {accuracy:.0%} ({n_correct}/{n_matched} labeled criteria matched exactly, "
        f"n={total_labeled} total labels, {n_missing} unmatched due to criterion-text drift)."
    )
    if worst_class:
        analysis_sentences.append(
            f"The weakest verdict class is {worst_class} at {per_class_recall[worst_class]:.0%} recall, "
            f"meaning the matcher most often mislabels this ground-truth category as something else."
        )
    if unknown_confused_as_definite:
        analysis_sentences.append(
            f"{unknown_confused_as_definite} case(s) where the true label is UNKNOWN (vignette genuinely "
            f"silent) were instead called MET or NOT_MET -- the matcher's most consequential failure mode, "
            f"since it manufactures certainty the source data doesn't support."
        )
    if n_wrong:
        analysis_sentences.append(
            f"The remaining {n_wrong - unknown_confused_as_definite} error(s) are boundary calls between "
            f"UNCERTAIN and a definite verdict, which is a lower-stakes miss than the UNKNOWN-to-definite case."
        )
    analysis_sentences.append(
        "Labels were hand-written from criterion text + vignette alone, blind to the matcher's own "
        "output, so this number reflects genuine matcher accuracy rather than label leakage."
    )
    analysis = " ".join(analysis_sentences)

    results = {
        "accuracy": round(accuracy, 4),
        "n": n_matched,
        "n_total_labels": total_labeled,
        "n_unmatched": n_missing,
        "confusion": confusion_out,
        "safety": safety,
        "errors": errors,
        "analysis": analysis,
    }

    with open(os.path.join(HERE, "eval_results.json"), "w") as f:
        json.dump(results, f, indent=2, ensure_ascii=False)

    print(f"Accuracy: {accuracy:.1%} ({n_correct}/{n_matched}, {n_missing} unmatched)")
    print(f"Confusion matrix: {json.dumps(confusion_out, indent=2)}")

    n_disq = safety["n_disqualifying_criteria"]
    n_missed = len(safety["missed_disqualifying_criteria"])
    print("\n--- Safety metrics (what overall accuracy hides) ---")
    if n_disq:
        print(f"Criteria that should rule the patient OUT: {n_disq}")
        print(f"  caught outright (FAIL)      : {n_disq - n_missed}")
        print(f"  deferred to review          : {safety['n_deferred_to_review']}  "
              f"(not decided; becomes a clarifying question)")
        print(f"  WRONGLY PASSED              : {safety['n_wrongly_passed']}  "
              f"(the failure that could actually hurt someone)")
    else:
        print("No disqualifying criteria in the label set.")
    fc = safety["false_certainty_rate"]
    if fc is not None:
        print(f"False certainty rate: {fc:.0%} "
              f"({len(safety['false_certainty_cases'])}/{safety['n_record_silent_criteria']})")
        print("  of the criteria the record is SILENT on, the share we answered anyway")

    print(f"\nAnalysis:\n{analysis}")
    print("\nWrote eval_results.json")


if __name__ == "__main__":
    main()
